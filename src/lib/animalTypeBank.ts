export function getAnimalTypeByStem(
  stemKey?: HeavenlyStemKey,
): AnimalTypeProfile | null {
  if (!stemKey) return null;

  const animalKey = stemToAnimalKeyMap[stemKey];

  return animalTypeBank[animalKey] ?? null;
}

export function getAnimalTypeByElement(
  element?: ElementKey,
): AnimalTypeProfile | null {
  if (!element) return null;

  const animalKey = elementFallbackAnimalKeyMap[element];

  return animalTypeBank[animalKey] ?? null;
}

