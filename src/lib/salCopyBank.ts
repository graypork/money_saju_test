function normalizeSalName(name: string) {
  return name.trim().replace(/\s+/g, "");
}

export function selectPrimarySalCopy(
  sals: string[],
  archetype?: string,
): SalCopy | null {
  void archetype;

  const normalizedSals = new Set(sals.map(normalizeSalName));
  const selectedName = SAL_PRIORITY.find((name) =>
    normalizedSals.has(normalizeSalName(name)),
  );

  if (!selectedName) return null;

  return salCopyBank[selectedName] ?? null;
}
